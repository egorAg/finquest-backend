// ─── Main ────────────────────────────────────────────────────────────────────

async function main() {
  console.log('🌱 Seeding database...');

  // Achievements
  console.log('  → Achievements...');
  for (const a of achievements) {
    await prisma.achievement.upsert({
      where: {
        // используем составной поиск через findFirst + create/update
        id: (await prisma.achievement.findFirst({
          where: { triggerType: a.triggerType, triggerValue: a.triggerValue ?? null, name: a.name },
        }))?.id ?? 'nonexistent',
      },
      update: a,
      create: a,
    });
  }
  console.log(`     ✓ ${achievements.length} achievements`);

  // Challenges
  console.log('  → Challenges...');
  // Удаляем старые глобальные челленджи и создаём заново
  await prisma.userChallenge.deleteMany({});
  await prisma.challenge.deleteMany({ where: { spaceId: null } });
  await prisma.challenge.createMany({ data: challenges });
  console.log(`     ✓ ${challenges.length} challenges`);

  // Knowledge articles
  console.log('  → Knowledge articles...');
  await prisma.knowledgeArticle.deleteMany({});
  await prisma.knowledgeArticle.createMany({ data: articles });
  console.log(`     ✓ ${articles.length} articles`);

  console.log('✅ Seed complete!');
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
